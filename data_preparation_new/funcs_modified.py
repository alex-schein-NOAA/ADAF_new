def create_obs_xarray(df_obs, df_coords, analysis_time, time_col='OBS_TIMESTAMP', lat_col='lat', lon_col='lon'):
    """
    Converts binned observations and a coordinate list into a single 3D xarray object.
    
    Coordinates:
    - obs_time_window: 0 (t-2), 1 (t-1), 2 (t)
    - latitude: from df_coords
    - longitude: from df_coords
    """
    # 1. Map timestamps to the window index (0, 1, 2)
    # Sorted order ensures the oldest time is 0 and newest is 2
    unique_times = sorted(df_obs[time_col].unique())
    time_map = {t: i for i, t in enumerate(unique_times)}
    
    df_prepared = df_obs.copy()
    
    # --- PRECISION CHECK: Round observations to 4 decimal places ---
    df_prepared[[lat_col, lon_col]] = df_prepared[[lat_col, lon_col]].round(4)
    
    df_prepared['obs_time_window'] = df_prepared[time_col].map(time_map)
    
    # 2. Set MultiIndex for the transition to xarray
    # We drop the original time column to keep the data variables clean
    df_prepared = df_prepared.drop(columns=[time_col]).set_index(['obs_time_window', lat_col, lon_col])
    
    # 3. Convert to xarray Dataset
    ds = df_prepared.to_xarray()
    
    # 4. Enforce the master coordinate list from df_coords
    # This ensures the Dataset contains all locations, filling missing ones with NaN
    # --- PRECISION CHECK: Round master coordinates to 4 decimal places ---
    master_lats = sorted(df_coords[lat_col].round(4).unique())
    master_lons = sorted(df_coords[lon_col].round(4).unique())
    
    ds = ds.reindex({
        lat_col: master_lats,
        lon_col: master_lons
    })
    
    # 5. Add standard metadata
    ds[lat_col].attrs = {'units': 'degrees_north'}
    ds[lon_col].attrs = {'units': 'degrees_east'}

    # 6. Add obs_mask and valid_time(=analysis_time), and make all NaNs 0
    valid_time_ns = np.datetime64(analysis_time, 'ns')
    ds_final = (
        ds.assign(
            obs_mask=(
                ds.to_array()
                .notnull()
                .all(dim="variable")           # True if all vars present at this point (sanity check - should already have been handled at the start by get_nnja_metar_data with drop_nans=True)
                .all(dim="obs_time_window")    # True if present across the whole window (sanity check - should already have been handled by filter_obs_by_temporal_completeness)
                .astype(int)                   # Final 2D binary mask (lat, lon)
            )
        )
        .fillna(0)                             # Replace NaNs with 0 in all data variables
        .assign_coords(valid_time=valid_time_ns) # To match ADAF example
    )
    
    return ds_final

