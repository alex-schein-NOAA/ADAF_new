def add_specific_humidity(df_input, dewpoint_col='MTRTMP.TMDP', pressure_col='sta_p'):
    """
    Calculates specific humidity from dewpoint (in Kelvin) and pressure. Pressure needs to have already been converted from Pa (native) to hPa.
    """
    df = df_input.copy()
    
    dp_k = df[dewpoint_col].values * units.K
    p_hpa = df[pressure_col].values * units.hPa
    
    sh_quantity = specific_humidity_from_dewpoint(p_hpa, dp_k)

    df[dewpoint_col] = sh_quantity.to('kg/kg').m
    df.rename(columns={dewpoint_col: 'sta_q'}, inplace=True)
    
    return df
    
