def fix_dataset_scaling_shifting(xr_data, adaf_var_str):
    """
    Fixes the scaling/shifting of the data of HRRR/RTMA to match ADAF. Only pressure and temperature need to be modified.
    Does NOT normalize RTMA data - that should be done after this function is run.

    Inputs:
        - xr_data --> either a HRRR or RTMA xarray dataset, resulting from fetch_and_regrid_hrrr_rtma
        - adaf_var_str --> valid options = 'sp', 't', 'q', 'u10', 'v10'
    """

    if adaf_var_str == 'sp':
        return xr_data/100 #convert Pa to hPa
    elif adaf_var_str == 't':
        xr_data = xr_data -273.15 #convert K to C
        return xr_data.where(~np.isclose(xr_data, -273.15), 0) #set all values close to -273.15 to 0
    elif adaf_var_str == 'q':
        return xr_data #no correction needed
    elif adaf_var_str == 'u10':
        return xr_data #no correction needed
    elif adaf_var_str == 'v10':
        return xr_data #no correction needed
    else:
        print(f"Invalid variable abbreviation - should only be 'sp', 't', 'q', 'u10', 'v10'")
    
