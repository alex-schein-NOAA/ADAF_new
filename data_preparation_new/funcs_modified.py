def check_nnja_metar_dataframe(df_master, analysis_time, list_of_metar_vars, obs_time_window=3, time_period=7, catalog_str='conv-adpsfc-NC000007'):
    """
    Checks if the requested interval [analysis_time - obs_time_window, analysis_time] is fully covered by the existing df_master. 
    If any part of that window falls outside the current df_master range, a new dataframe is loaded.
    """
    start_time = analysis_time - dt.timedelta(hours=obs_time_window)

    df_times = df_master['OBS_TIMESTAMP'].dt.tz_localize(None).dt.floor('h')
    df_min = df_times.min()
    df_max = df_times.max()

    # Check if the required window is outside the current range
    # If the window starts earlier than our min, or ends later than our max, we need a reload
    if (start_time < df_min) or (analysis_time > df_max):
        print(f"Requested window [{start_time} to {analysis_time}] falls outside current coverage [{df_min} to {df_max}]. Loading new data...")
        
        df_master = get_nnja_metar_dataframe(analysis_time, 
                                             list_of_metar_vars, 
                                             time_period=time_period, 
                                             catalog_str=catalog_str)
        
    return df_master

