def get_missing_obs_hours(df_master, analysis_time, obs_time_window=3, time_col='OBS_TIMESTAMP'):
    """
    Returns the list of required hourly timestamps that are missing from df_master for a given analysis time.
    Required hours are [analysis_time-(obs_time_window-1), ..., analysis_time].
    """
    if time_col not in df_master.columns:
        return list(pd.date_range(end=analysis_time, periods=obs_time_window, freq='h').to_pydatetime())

    # Coerce to UTC to handle both naive and tz-aware timestamps consistently.
    obs_times = pd.to_datetime(df_master[time_col], errors='coerce', utc=True)
    obs_times = obs_times.dropna().dt.tz_convert(None).dt.floor('h')
    available_hours = set(obs_times.unique().tolist())

    required_hours = pd.date_range(end=analysis_time, periods=obs_time_window, freq='h')
    missing_hours = [ts.to_pydatetime() for ts in required_hours if ts not in available_hours]

    return missing_hours
    
