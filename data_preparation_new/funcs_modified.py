def get_grid_alt_and_regrid(topo_filepath, ds_adaf_lats_lons, make_new_regridder=False):
    """
    Remake of get_grid_alt for the URMA native 2.5km topography
    """
    topo = xr.open_dataset(topo_filepath, engine="cfgrib", backend_kwargs={"indexpath": ""})
    topo_lon_mask = (topo.longitude>=ds_adaf_lats_lons.lon.data[0])&(topo.longitude<=ds_adaf_lats_lons.lon.data[-1])
    topo_lat_mask = (topo.latitude>=ds_adaf_lats_lons.lat.data[0])&(topo.latitude<=ds_adaf_lats_lons.lat.data[-1])
    topo = topo["orog"].where((topo_lat_mask)&(topo_lon_mask), drop=True) #'orog' valid for URMA; may need to change if using another terrain field
    if make_new_regridder:
        print(f"Making new topo regridder...")
        regridder_topo = xesmf.Regridder(topo, ds_adaf_lats_lons, 'bilinear')
        regridder_topo.to_netcdf(f"regridder_topo.nc")
    else:
        regridder_topo = xesmf.Regridder(topo, ds_adaf_lats_lons, 'bilinear', weights=f"regridder_topo.nc")

    return regridder_topo(topo, keep_attrs=True)

