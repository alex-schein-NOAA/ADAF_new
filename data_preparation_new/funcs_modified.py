def convert_wind_to_uv(df_input, speed_col='MTRWND.WSPD', dir_col='MTRWND.WDIR', tolerance=1e-6):
    """
    Converts wind speed and direction to U and V components, overwriting the original columns in the process.
    """
    
    df = df_input.copy()
    
    # Convert direction to radians
    rad = np.deg2rad(df[dir_col])
    
    # Calculate U and V components
    # Store them in temporary variables first to avoid using the overwritten speed_col for the second calculation
    u = -df[speed_col] * np.sin(rad)
    v = -df[speed_col] * np.cos(rad)

    # Round tiny values to 0
    u[u.abs()<tolerance] = 0
    v[v.abs()<tolerance] = 0
    
    # Overwrite the original columns
    df[speed_col] = u
    df[dir_col] = v
    
    # Rename columns to reflect the new data
    df.rename(columns={speed_col: 'sta_u10', dir_col: 'sta_v10'}, inplace=True)
    
    return df
    
