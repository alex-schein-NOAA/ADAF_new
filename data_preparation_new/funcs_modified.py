def select_and_clean_metar_obs(df_master, analysis_time, obs_time_window, drop_nans=True, convert_units=True, rename_data_vars=True):
    """
    Subselects a known good (in terms of time range) df_master down to the needed time window, and performs data cleanup.
    
    - If drop_nans=True (default), all rows with at least one NaN observation are dropped.
    - If convert_units=True (default), temperature and pressure are changed (K-->C and Pa-->hPa respectively)
    - If rename_data_vars=True (default), temperature and pressure get renamed to 'sta_t' and 'sta_p'.
        Note: LAT and LON are always renamed to lat and lon here
    """
    time_sel_str_start = (analysis_time-dt.timedelta(hours=obs_time_window)+dt.timedelta(seconds=1)).strftime("%Y-%m-%d %H:%M:%S") #start is delayed. Addition of 1 second is needed to cull some weird stragglers that get rounded up to t-3 which we don't want
    time_sel_str_end = analysis_time.strftime("%Y-%m-%d %H:%M:%S") 

    df = df_master[df_master['OBS_TIMESTAMP'].between(time_sel_str_start, time_sel_str_end)] #filter to only obs_window times
    
    df.rename(columns={"LAT": "lat", "LON": "lon"}, inplace=True)
    
    if drop_nans: # QC to drop any points where any of the vars are NaN (or coords, though in practice this never happens)
        df = df.dropna()
    if convert_units:
        df["MTRPRS.ALSE"] = df["MTRPRS.ALSE"]*0.01 #Pa to hPa 
        df["MTRTMP.TMDB"] = df["MTRTMP.TMDB"]-273.15 #K to C. Don't have to worry about dewpoint temp as that gets overwritten
    if rename_data_vars:
        df.rename(columns={"MTRTMP.TMDB": "sta_t", "MTRPRS.ALSE": "sta_p"}, inplace=True) #the other vars are yet to be calculated and are renamed in those functions

    return df
    
