def filter_obs_by_temporal_completeness(df_input, obs_time_window, precision=4):
    """
    Checks if each (lat, lon) point has a row for every hour in the window.
    Points missing any hour are removed entirely.
    """
    # 1. Work on a copy to ensure we don't modify the input df directly
    df = df_input.copy()
    
    # 2. Round coordinates to prevent precision-related grouping errors
    df[['lat', 'lon']] = df[['lat', 'lon']].round(precision)
    
    # 3. Count unique timestamps for each coordinate pair
    # transform('nunique') assigns the total count of unique hours to every row in that group
    counts = df.groupby(['lat', 'lon'])['OBS_TIMESTAMP'].transform('nunique')
    
    # 4. Keep only the points where the count matches the required window size
    # If a point has 4 UTC and 6 UTC but is missing 5 UTC, its count will be 2.
    # Since 2 != 3 (if window is 3), those rows will be dropped.
    df_filtered = df[counts == obs_time_window]
    
    return df_filtered
    
