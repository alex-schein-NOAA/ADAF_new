def fetch_and_regrid_hrrr_rtma(adaf_var_str, ds_adaf_lats_lons, hrrr_filepath, rtma_filepath, make_new_regridders=False):
    """
    Reads in and fetches the appropriate HRRR and RTMA field based on the given variable string (ADAF standard naming), then regrids to the ADAF grid.
    Does the same for topography - default is the native 2.5 km terrain field from URMA. 
    Returns 2 full regridded xarray objects, one each for HRRR and RTMA of the variable requested. Further processing should happen in the calling function.
    Renames the field in the new xarray objects from the original name to the ADAF name, for convenience in the main workflow.

    Inputs:
        - adaf_var_str --> valid options = 'sp', 't', 'q', 'u10', 'v10'
        - ds_adaf_lats_lons --> xarray dataset of only ADAF lats/lons as generated by arange in main. Used for regridding
        - hrrr_filepath --> path to HRRR .grib2 file to be opened. Assumed to be a complete file, i.e. not already subset to a variable/variables
        - rtma_filepath --> path to RTMA .grib2 file to be opened. Assumed to be a complete file, i.e. not already subset to a variable/variables
        - make_new_regridders --> bool to determine if new regridders need to be built from HRRR/RTMA --> ADAF grid. Default = False. Should only be True if grid(s) change
        
    Outputs:
        xr_hrrr_regridded, xr_rtma_regridded
    """

    dict_level_selection = {"sp":{'filter_by_keys':{'stepType': 'instant', 'typeOfLevel': 'surface'}},
                            "t":{'filter_by_keys':{'typeOfLevel': 'heightAboveGround','level':2}}, 
                            "q":{'filter_by_keys':{'typeOfLevel': 'heightAboveGround','level':2}},
                            "u10":{'filter_by_keys':{'typeOfLevel': 'heightAboveGround','level':10}},
                            "v10":{'filter_by_keys':{'typeOfLevel': 'heightAboveGround','level':10}}}

    dict_adaf_translate = {'sp':'sp', 
                           't':'t2m', 
                           'q':'sh2', 
                           'u10':'u10', 
                           'v10':'v10'}

    # Load datasets and regridders (regridders should be precalculated and on disk - if not, rerun (bilinear!) )
    hrrr_backend_kwargs = dict(dict_level_selection[adaf_var_str])
    hrrr_backend_kwargs["indexpath"] = ""
    rtma_backend_kwargs = dict(dict_level_selection[adaf_var_str])
    rtma_backend_kwargs["indexpath"] = ""

    xr_hrrr = xr.open_dataset(hrrr_filepath, engine='cfgrib', backend_kwargs=hrrr_backend_kwargs)
    xr_rtma = xr.open_dataset(rtma_filepath, engine='cfgrib', backend_kwargs=rtma_backend_kwargs)

    if make_new_regridders:
        print(f"Making new HRRR and RTMA regridders...")
        regridder_hrrr = xesmf.Regridder(xr_hrrr, ds_adaf_lats_lons, 'bilinear')
        regridder_hrrr.to_netcdf(f"regridder_hrrr.nc")
        regridder_rtma = xesmf.Regridder(xr_rtma, ds_adaf_lats_lons, 'bilinear')
        regridder_rtma.to_netcdf(f"regridder_rtma.nc")
    else:
        regridder_hrrr = xesmf.Regridder(xr_hrrr, ds_adaf_lats_lons, 'bilinear', weights=f"regridder_hrrr.nc")
        regridder_rtma = xesmf.Regridder(xr_rtma, ds_adaf_lats_lons, 'bilinear', weights=f"regridder_rtma.nc")
    
    # Rename the field to the ADAF name (has to be done on the full dataset before it gets subset to a DataArray)
    xr_hrrr = xr_hrrr.rename_vars({dict_adaf_translate[adaf_var_str] : adaf_var_str})
    xr_rtma = xr_rtma.rename_vars({dict_adaf_translate[adaf_var_str] : adaf_var_str})

    # Subset down to only the variable we care about
    xr_hrrr = xr_hrrr[adaf_var_str]
    xr_rtma = xr_rtma[adaf_var_str]
    
    xr_hrrr_regridded = regridder_hrrr(xr_hrrr, keep_attrs=True)
    xr_rtma_regridded = regridder_rtma(xr_rtma, keep_attrs=True)

    return xr_hrrr_regridded, xr_rtma_regridded
    

